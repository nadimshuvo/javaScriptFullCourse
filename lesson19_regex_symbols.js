// ৪. ক্যারেক্টার সেট (Character Sets)
const symbols4 = {
    "[abc]": "a অথবা b অথবা c এর মধ্যে যেকোনো একটি",
    "[^abc]": "a, b, বা c ছাড়া অন্য যেকোনো কিছু",
    "[a-z]": "a থেকে z পর্যন্ত যেকোনো ছোট হাতের অক্ষর",
    "[A-Z]": "A থেকে Z পর্যন্ত যেকোনো বড় হাতের অক্ষর",
    "[0-9]": "০ থেকে ৯ পর্যন্ত যেকোনো সংখ্যা"
};

/**
 * বাস্তব উদাহরণ (Practical Example):
 * একটি পাসওয়ার্ড চেক করা যা অন্তত ৮ অক্ষরের হবে এবং তাতে অন্তত ১টি সংখ্যা থাকবে।
 */
const passwordRegex = /^(?=.*\d).{8,}$/; 

console.log("পাসওয়ার্ড টেস্ট ('pass123'):", passwordRegex.test("pass123")); // false (৮টির কম)
console.log("পাসওয়ার্ড টেস্ট ('password'):", passwordRegex.test("password")); // false (সংখ্যা নেই)
console.log("পাসওয়ার্ড টেস্ট ('mypass123'):", passwordRegex.test("mypass123")); // true

